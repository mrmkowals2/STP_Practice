def add_three_to_five_numbers(a, b, c, x=1, y=2):
    """
    Returns sum of given arguments.
    :param a: float.
    :param b: float.
    :param c: float.
    :param x: float.
    :param y: float.
    :return: float.
    """
    return a + b + c + x + y

print(add_three_to_five_numbers(1,2,3,0))                       
